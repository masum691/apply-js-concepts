function isEven(number){
    if(number % 2 == 0){
        return true;
    }
    else{
        return false;
    }
}
const herNumber = 4676;
const myNumber = 8977;
const isMyNumberEven = isEven(herNumber);
console.log(isMyNumberEven);


// odd number
function isOdd(num){
    if(num % 2 != 0){
        return true;
    }
    return false;
}
var numberIs = 222;
var odd = isOdd(numberIs);
console.log(odd);
















































